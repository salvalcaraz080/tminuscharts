def make_t(lang: str):
    """Return a translator function bound to the given language."""
    table = TRANSLATIONS.get(lang, TRANSLATIONS["en"])

    def t(key: str, **kwargs) -> str:
        value = table.get(key, key)
        return value.format(**kwargs) if kwargs else value

    return t
